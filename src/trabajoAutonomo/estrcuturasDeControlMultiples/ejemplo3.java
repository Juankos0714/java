package trabajoAutonomo.estrcuturasDeControlMultiples;

public class ejemplo3 {
    public static void main(String[] args) {
        for (int a = 1; a <= 10; a++) {
            if (a % 2 == 0) {
                System.out.println(a + " es par.");
            } else {
                System.out.println(a + " es impar.");
            }
        }
    }
}

